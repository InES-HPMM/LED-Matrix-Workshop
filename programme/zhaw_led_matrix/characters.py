    def convert_str(text):
        """convert string to list of led matrix characters"""

        def apply_x_offset(coords, val):
            for i, coord in enumerate(coords):
                coords[i] = coord[0] + val, coord[1]

        result = [None] * len(text)
        length = 0
        for i, char in enumerate(text):
            char = CharacterTable.CHAR_MAPPING[char].copy()
            apply_x_offset(char, length)
            length += char.width + 1
            result[i] = char
        return result, length - 1
